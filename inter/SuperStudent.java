package inter;

public abstract class SuperStudent {
    int  uniform;
    int time;
    protected int cla;

    int rno = 15;


    public SuperStudent(int uniform, int time, int cla, int rno) {
        this.uniform = uniform;
        this.time = time;
        this.cla = cla;
        this.rno = rno;
    }

}
